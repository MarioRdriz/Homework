print ("\n\nPrograma que lea un número entero y nos indique a que día de la semana pertenece")
n=int(input("Digita un número: "))

if n == 1:
    print ("Es el día Lunes")
elif n == 2:
    print ("Es el día Martes")
elif n == 3:
    print ("Es el día Miércoles")
elif n == 4:
    print ("Es el día Jueves")
elif n == 5:
    print ("Es el día Viernes")
elif n == 6:
    print ("Es el día Sábado")
elif n == 7:
    print ("Es el día Domingo")
else:
    print ("XD")